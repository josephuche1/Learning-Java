
public class Rectangle {
	// Parameters
     public double width;
     public double height;
     public String color;
     
     // Constructors
     public Rectangle() {
    	 width = 1;
    	 height = 1;
    	 color = "white";
     }
     
     public Rectangle(double width, double height) {
    	 this.width = width;
    	 this.height = height;
    	 this.color = "white";
     }
}
